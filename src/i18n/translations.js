export function getTranslations(language) {
  return translations[language] || translations.en;
}

export function interpolate(template, values = {}) {
  return Object.entries(values).reduce(
    (output, [key, value]) => output.replaceAll(`{${key}}`, value),
    template,
  );
}

export function displayNodeLabel(nodeOrId, nodeMap = {}, t = translations.en, useShort = false) {
  const node = typeof nodeOrId === 'string' ? nodeMap[nodeOrId] : nodeOrId;
  if (!node) return String(nodeOrId || '');
  const source = useShort ? node.shortLabel || node.label : node.label;
  if (t.lang === 'en') return source;
  if (useShort && t.shortLabels?.[source]) return t.shortLabels[source];
  return t.nodeLabels?.[source] || source;
}

export function displayAirportText(airport, field, t = translations.en) {
  if (!airport) return '';
  if (t.lang === 'en') return airport[field] || '';
  if (field === 'summary' || field === 'layoutSummary') return t.airportGuideSummary;
  if (field === 'beforeMoveTip') return t.genericBeforeMove;
  if (field === 'securityNotes') return t.genericSecurityNotes;
  if (field === 'watchOutTip') return t.genericWatchOutTip;
  if (field === 'currentAdvisory') return t.genericAdvisory;
  return airport[field] || '';
}

export function translateDataNote(note, t = translations.en) {
  if (!note) return '';
  if (t.lang === 'en') return note;

  const normalized = note.trim();
  const common = new Map([
    ['Official travel time not specified by the extracted official source; app uses this only for routing order.', t.sourceTimeNotSpecifiedNote],
    ['Official time not extracted; verify live signs.', t.verifySignsNote],
    ['Verify current official signs and airport pages before travel.', t.verifySignsNote],
    ['Official time not extracted.', t.sourceTimeNotSpecifiedNote],
    ['Official time not specified.', t.sourceTimeNotSpecifiedNote],
  ]);

  if (common.has(normalized)) return common.get(normalized);
  if (t.dataNotes?.[normalized]) return t.dataNotes[normalized];
  if (normalized.toLowerCase().includes('official') && normalized.toLowerCase().includes('time')) {
    return t.sourceTimeNotSpecifiedNote;
  }
  if (normalized.toLowerCase().includes('verify') || normalized.toLowerCase().includes('manual')) {
    return t.verifySignsNote;
  }
  return '';
}
